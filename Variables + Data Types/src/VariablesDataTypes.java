public class VariablesDataTypes {
    public static void main(String[] args) throws Exception {
        
        int num = 5; //sets num as 5 as an integer
        System.out.println(num); //prints num

        // set variables before asking to print the variable!!!

        double decimal = 2.4; //double is a decimal point
        System.out.println(decimal);

        boolean fact = true; //boolean can be true or false
        char c = 't'; //only for 1 character, thus, char
                      //char can be set to a number too
        String str = "Qwop"; //can hold anything

        //int, double, boolean and char are primitive - char is not

        int James = 5; 
        System.out.println(5); //prints 5 which is same as James
    }
}
